## 描述
# 编写程序，计算2个正整数的和、差、积、商并输出。题目保证输入和输出全部在整型范围内且除数不为0。**
# 在第一行中接收一个正整数A**
# **在第一行中接收一个正整数B**
# **主要是eval()函数的使用，eval可以脱掉一层的引号。**
a = eval(input())
b = eval(input())
print("{0} + {1} = {2}".format(a, b, a + b))
print("{0} - {1} = {2}".format(a, b, a - b))
print("{0} * {1} = {2}".format(a, b, a * b))
print("{0} / {1} = {2}".format(a, b, a / b))
